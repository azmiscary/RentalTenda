    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
       try {
if (txt_kode_tenda.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Kode film belum diisi");
txt_kode_tenda.requestFocus();
} else
if (txt_Ukuran.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Judul film belum diisi");
txt_Ukuran.requestFocus();
} else
if (String.valueOf(cmb_jenis.getSelectedItem())==".: Pilih Judul :."){
JOptionPane.showMessageDialog(null,"Genre film belum diisi");
cmb_jenis.requestFocus();
} else
if (txt_harga_sewa.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Genre film belum diisi");
txt_harga_sewa.requestFocus();
} else
if (txt_stok_tenda.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Asal negara film belum diisi");
txt_stok_tenda.requestFocus();
}else
if (txt_pencarian_pesanan.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Stok film belum diisi");
txt_pencarian_pesanan.requestFocus();
}else
if (new ConfigDB().duplikasiData("film","KodeFilm",txt_kode_tenda.getText())==true){
JOptionPane.showMessageDialog(null,"Kode Film sudah terdaftar");
Statement st = new ConfigDB().koneksi.createStatement();
ResultSet rs = st.executeQuery("SELECT*FROM film WHERE KodeFilm='"+txt_kode_tenda.getText()+"'");
if (rs.next()){
txt_Ukuran.setText(rs.getString("judul"));
cmb_jenis.setSelectedItem(String.valueOf(rs.getString("genre")));
txt_harga_sewa.setText(rs.getString("tahun"));
txt_stok_tenda.setText(rs.getString("asal"));
}
}else{
String SQL="INSERT INTO film VALUES ('"+txt_kode_tenda.getText()+"','"+txt_Ukuran.getText()+"','"+
String.valueOf(cmb_jenis.getSelectedItem()) +"','"+txt_harga_sewa.getText()+"','"+txt_stok_tenda.getText()+"')"; 
new ConfigDB().simpanData(SQL);
new ConfigDB().tampilTabel(judulKolom, sqlFilm, TabelTenda);
new ConfigDB().aturLebarKolom(TabelTenda, lebarKolom);
}
} catch (Exception e) {
}
    }//GEN-LAST:event_btn_simpanActionPerformed

