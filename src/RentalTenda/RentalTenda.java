    private void btn_UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UbahActionPerformed
        // TODO add your handling code here:
        try {
if (
(txt_kode_tenda.getText().isEmpty()) && (txt_Ukuran.getText().isEmpty()) &&
(txt_harga_sewa.getText().isEmpty()) && (txt_stok_tenda.getText().isEmpty()) &&
(String.valueOf(cmb_jenis.getSelectedItem())==".: Pilih Judul :.")
)
{
JOptionPane.showMessageDialog(null,"Silahkan Pilih Data dulu untuk proses Ubah");
TabelTenda.requestFocus();
} else
{
String SQL="UPDATE film SET judul='"+txt_Ukuran.getText()+"', genre='"+String.valueOf(cmb_jenis.getSelectedItem())
+"', tahun='"+txt_harga_sewa.getText()+"', asal='"+txt_stok_tenda.getText()+"', stok='"+"' WHERE KodeTenda='"+txt_kode_tenda.getText()+"'";
new ConfigDB().ubahData(SQL);
new ConfigDB().tampilTabel(judulKolom, sqlFilm, TabelTenda);
new ConfigDB().aturLebarKolom(TabelTenda, lebarKolom);
}
} catch (Exception e)
{
System.out.print(e.toString());
}

    
    
    
    }//GEN-LAST:event_btn_UbahActionPerformed

